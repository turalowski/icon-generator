import { type NextPage } from "next";
import clsx from "clsx";
import Head from "next/head";
import { FormGroup, Input } from "~/component";
import { useState } from "react";
import { api } from "~/utils/api";
import Image from "next/image";

const Generate: NextPage = () => {
  const [form, setForm] = useState({
    prompt: "",
  });
  const [imageUrl, setImageUrl] = useState("");

  const generateIcon = api.generate.generateIcon.useMutation({
    onSuccess(data) {
      console.log("Mutation finished", data.imageUrl);
      if (!data.imageUrl) return;
      setImageUrl(data.imageUrl);
    },
  });

  function updateForm(key: string) {
    return function (e: React.ChangeEvent<HTMLInputElement>) {
      setForm((prev) => ({
        ...prev,
        [key]: e.target.value,
      }));
    };
  }

  async function onSubmit(event: React.FormEvent) {
    event.preventDefault();
    await generateIcon.mutateAsync({
      prompt: form.prompt,
    });
    setForm({ prompt: "" });
  }

  return (
    <>
      <Head>
        <title>Create T3 App</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex min-h-screen flex-col items-center justify-center">
        <form onSubmit={onSubmit} className={clsx("flex flex-col gap-4")}>
          <FormGroup>
            <label>Prompt</label>
            <Input value={form.prompt} onChange={updateForm("prompt")} />
          </FormGroup>
          <input
            className={clsx(
              "bg-blue-400 hover:bg-blue-500",
              "px-4 py-2",
              "rounded"
            )}
            type="submit"
          ></input>
        </form>
        {!!imageUrl && (
          <Image
            width={100}
            height={100}
            alt="An image of generated prompt"
            src={imageUrl}
          />
        )}
      </main>
    </>
  );
};

export default Generate;
